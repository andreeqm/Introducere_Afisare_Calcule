"""Într-o gospodărie sunt 4 găini. Introduceţi în calculator prin variabilele a, b, c, d
numărul de ouă pe care-l dă fiecare găină într-o zi. Afişaţi câte ouă se obţin într-o
săptămână. """
a=int(input("Introduceti a=:"))
b=int(input("Introduceti b=:"))
c=int(input("Introduceti c=:"))
d=int(input("Introduceti d=:"))
sa=a*7
sb=b*7
sc=c*7
sd=d*7
print("Prima gaina face intr-o saptamana",sa,"oua")
print("A doua gaina face intr-o saptamana",sb,"oua")
print("A treia gaina face intr-o saptamana",sc,"oua")
print("A patra gaina face intr-o saptamana",sd,"oua")