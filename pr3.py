"""Într-un autobuz care pleacă în excursie sunt 7 copii. De la încă două şcoli urcă alţi
copii, numărul acestora citindu-se de la tastatura. Câţi copii au plecat în excursie? """
n=7
m=int(input("Inroduceti numarul de copii de la prima scoala:"))
l=int(input("Introduceti numarul de copii de la a doua scoala:"))
total=n+m+l
print("In excursie au plecat",total,"copii")